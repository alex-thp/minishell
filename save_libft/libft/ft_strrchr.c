#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*cps;

	i = ft_strlen(s);
	cps = (char *)s;
	while (i >= 0)
	{
		if (cps[i] == (char)c)
			return (cps + i);
		i--;
	}
	return (NULL);
}
